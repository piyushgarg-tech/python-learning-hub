"""
NumPy Array Basics
"""

import numpy as np

arr = np.array([1, 2, 3, 4, 5])

print(arr)                  # Array

print(arr.dtype)            # Data Type

print(arr.shape)            # Shape

print(arr.ndim)             # Dimensions

print(arr.size)             # Total Elements

print(arr.sum())            # Sum

print(arr.mean())           # Mean

print(arr.max())            # Maximum

print(arr.min())            # Minimum

print(arr * 2)              # Vectorized Operation