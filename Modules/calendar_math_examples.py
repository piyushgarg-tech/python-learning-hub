"""
Calendar & Math Quick Revision
"""

import calendar
import math

# Calendar

print(calendar.isleap(2024))      # Leap Year

print(calendar.month(2025, 6))    # Month

# Math

print(math.sqrt(25))              # Square Root

print(math.ceil(4.2))             # Upper Integer

print(math.floor(4.9))            # Lower Integer

print(math.factorial(5))          # Factorial

print(math.gcd(18, 24))           # GCD

print(math.pi)                    # Pi

print(math.sin(math.radians(90))) # Sin

# Useful Built-ins

print(abs(-10))                   # Absolute

print(round(3.14159, 2))          # Round

print(pow(2, 5))                  # Power